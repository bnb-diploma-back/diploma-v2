CREATE TABLE student_tasks
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id      BIGINT       NOT NULL REFERENCES students (id),
    syllabus_id     BIGINT       NOT NULL REFERENCES syllabi (id),
    week_number     INTEGER      NOT NULL,
    title           VARCHAR(255) NOT NULL,
    description     TEXT,
    instructions    TEXT,
    task_type       VARCHAR(255) NOT NULL DEFAULT 'HOMEWORK',
    status          VARCHAR(255) NOT NULL DEFAULT 'PENDING',
    due_date        DATE,
    estimated_hours INTEGER,
    max_score       INTEGER,
    score           INTEGER,
    feedback        TEXT,
    submission_url  TEXT,
    submitted_at    TIMESTAMP,
    created_at      TIMESTAMP,
    updated_at      TIMESTAMP
);

-- For existing databases where student_tasks was created without estimated_hours
ALTER TABLE student_tasks ADD COLUMN IF NOT EXISTS estimated_hours INTEGER;

CREATE TABLE departments
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code        VARCHAR(255) NOT NULL UNIQUE,
    name        VARCHAR(255) NOT NULL,
    description TEXT,
    created_at  TIMESTAMP,
    updated_at  TIMESTAMP
);

CREATE TABLE majors
(
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code          VARCHAR(255) NOT NULL UNIQUE,
    name          VARCHAR(255) NOT NULL,
    description   TEXT,
    department_id BIGINT       NOT NULL REFERENCES departments (id),
    created_at    TIMESTAMP,
    updated_at    TIMESTAMP
);

-- Replace plain string department/major on students with FK references
ALTER TABLE students ADD COLUMN IF NOT EXISTS department_id BIGINT REFERENCES departments (id);
ALTER TABLE students ADD COLUMN IF NOT EXISTS major_id BIGINT REFERENCES majors (id);
ALTER TABLE students DROP COLUMN IF EXISTS department;
ALTER TABLE students DROP COLUMN IF EXISTS major;

CREATE TABLE users
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email       VARCHAR(255) NOT NULL UNIQUE,
    password    VARCHAR(255) NOT NULL,
    first_name  VARCHAR(255) NOT NULL,
    last_name   VARCHAR(255) NOT NULL,
    role        VARCHAR(255) NOT NULL DEFAULT 'STUDENT',
    student_id  BIGINT UNIQUE REFERENCES students (id),
    created_at  TIMESTAMP,
    updated_at  TIMESTAMP
);

CREATE TABLE sessions
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token       TEXT         NOT NULL UNIQUE,
    user_id     BIGINT       NOT NULL REFERENCES users (id),
    expires_at  TIMESTAMP    NOT NULL,
    revoked     BOOLEAN      NOT NULL DEFAULT FALSE,
    ip_address  VARCHAR(255),
    user_agent  TEXT,
    created_at  TIMESTAMP
);

CREATE TABLE weekly_organizers
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id      BIGINT  NOT NULL REFERENCES students (id),
    week_number     INTEGER NOT NULL,
    ai_response     TEXT    NOT NULL,
    created_at      TIMESTAMP,
    updated_at      TIMESTAMP,
    UNIQUE (student_id, week_number)
);